// MARK: - UNUserNotificationCenterDelegate

extension AppDelegate: UNUserNotificationCenterDelegate {
    
    /// Handle notifications when app is in foreground
    func userNotificationCenter(
        _ center: UNUserNotificationCenter,
        willPresent notification: UNNotification,
        withCompletionHandler completionHandler: @escaping (UNNotificationPresentationOptions) -> Void
    ) {
        print("ðŸ”” Notification received in foreground: \(notification.request.content.title)")
        
        // Present notification with sound and badge even when app is in foreground
        if #available(iOS 14.0, *) {
            completionHandler([.banner, .sound, .badge])
        } else {
            completionHandler([.sound, .badge])
        }
    }
    
    /// Handle user interaction with notifications
    func userNotificationCenter(
        _ center: UNUserNotificationCenter,
        didReceive response: UNNotificationResponse,
        withCompletionHandler completionHandler: @escaping () -> Void
    ) {
        let userInfo = response.notification.request.content.userInfo
        
        print("ðŸ‘† User tapped notification: \(response.notification.request.content.title)")
        print("ðŸ“¦ Notification user info: \(userInfo)")
        
        // Let Capacitor handle the notification response
        NotificationCenter.default.post(
            name: Notification.Name("LocalNotificationTapped"),
            object: nil,
            userInfo: userInfo
        )
        
        completionHandler()
    }
